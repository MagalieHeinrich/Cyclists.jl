md"""
### Section 1: Introduction & The Problem (The Cyclist "Wobble")

* A group of cyclists "wobbles" because each cyclist adjusts their speed and distance to the speed of the rider in front of them.
* A first naive ODE discribing this is given by

$$\frac{dv_i}{dt} = \alpha (v_{i-1} - v_i) + \beta (d_i - d_{\text{safe}})$$

with $\alpha$: Rider's reaction to changing paces. A higher $\alpha$ means a highly attentive rider.

and $\beta$: Rider's desire to maintain the perfect draft. A higher $\beta$ means a rider who is aggressive about staying tight on the wheel ahead. (lower $\beta$ means a more relaxed rider who doesn't mind if the gap grows or shrinks a bit)
"""

