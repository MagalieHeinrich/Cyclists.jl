# ╔═╡ 37e712ba-c2bf-48f7-8078-d0ef04596f26
 function primitive_cyclists!(du, u, p_composite, t)

        p_list = p_composite.riders
        
        n = Int(length(u)/2)
        x = @view u[1:n]
        v = @view u[n+1:end]
        dx = @view du[1:n]
        dv = @view du[n+1:end]
        
        v_leader = 30 / 3.6
        dx[1] = v[1]
        v[1] = v_leader
        dv[1] = 0.0

        for i in 2:n
            α, β, d_safe = p_list[i-1] 
            dist = x[i-1] - x[i]
            dv[i] = α * (v[i-1] - v[i]) + β * (dist - d_safe)
            
            dx[i] = v[i]
        end
    end

