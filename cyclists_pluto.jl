function plot_leader_distance(sol_sim, t_steps, n_riders, v_labels)
    # Calculate x[1] - x[i] for every time step and every rider
    cum_dist_vals = [sol_sim(t)[1] - sol_sim(t)[i] for t in t_steps, i in 1:n_riders]
    
    return plot(
        t_steps, 
        cum_dist_vals, 
        title="Cumulative Distance to Leader", 
        xlabel="Time [s]", 
        ylabel="Distance Behind [m]", 
        label=v_labels, 
        legend=:outerright, 
        lw=1.5
    )
end

