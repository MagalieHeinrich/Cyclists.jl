md"""
### Section 4: Introducing Wind Dynamics (Deterministic vs. Stochastic)

* We now have a stable physical baseline but it's a WINDY day...
* We introduce aerodynamic drag variations via two different wind models

#### The Sinus Wind (Deterministic)

* We use a sinus-wind wave.  
$$W(t) = A \sin(\omega t)$$



#### The Ornstein-Uhlenbeck (OU) Wind (SDE)

* Sine wind is not so realistic
* We introduced a stochastic wind model using an Ornstein-Uhlenbeck process solved with Euler-Maruyama.

* Every timestep, a random burst of chaotic noise pushes the wind speed up or down (Diffusion).

* But we also have some "elastic band" (mean reversion, $\mu - W(t)$) mechanic that brings the wind back to its target speed. 

$$dW(t) = \underbrace{\theta (\mu - W(t))dt}_{\text{Drift}} + \underbrace{\sigma dW_t}_{\text{Diffusion}}$$

with $\theta$: speed of mean reversion, 
$\mu$: baseline wind speed, 
$\sigma dW_t$: random noise.

"""


